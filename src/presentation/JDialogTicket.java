    private void remplireInfoTicket(Transaction trans) {
        this.idVente = trans.getIdVente();
        this.jTextFieldIdCassier.setText(String.valueOf(trans.getIdVendeur()));
        this.jTextFieldTotal.setText(formatter.format(trans.getTotal()));
        this.jTextFieldDate.setText(trans.getDateString());
        myModelTicket.setMyList(daoTicket.selectTicket(idVente));
        
        int nbArticle = 0;
        int nbArticleDif = 0;

        double montantTVA = 0;
        double prixHTVA = 0;
        for (Produit ticket : daoTicket.selectTicket(trans.getIdVente())) {
            nbArticle += ticket.getQuantite();
            montantTVA += ticket.getMontantTva();
            prixHTVA += ticket.getPrixHTVA();
            nbArticleDif++;
        }
        jTextFieldNbArticle.setText(String.valueOf(nbArticle));
        jTextFieldNbArticleDif.setText(String.valueOf(nbArticleDif));
        jTextFieldMontantTVA.setText(formatter.format(montantTVA));
        jTextFieldPrixHTVA.setText(formatter.format(prixHTVA));
        jTextFieldRemise.setText(formatter.format(daoVente.remiseVente(idVente)));
    }

