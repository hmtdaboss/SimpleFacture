    private boolean dejaDansLeTicket(String codeBarre) {
        boolean exist = false;
        for (Produit ticket : daoTicket.selectTicket(idVente)) {
            if (ticket.getCodebarre().compareTo(codeBarre) == 0) {
                exist = true;
                break;
            }
        }
        return exist;
    }

    private void jButton12ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton12ActionPerformed
        String codeBarre = JOptionPane.showInputDialog(this,
                "Scanner le produit ", null);
        if (codeBarre != null) {

            if (!dejaDansLeTicket(codeBarre)) {
                Produit pro = daoProduit.uneVenteProduit(codeBarre);
                if (pro != null) {
                    double tva = ((pro.getTva() / 100) + 1);
                    double prixHTArt = pro.getPrixVente() / tva;
                    double montantTvaArti = (pro.getPrixVente() - prixHTArt);
                    Vente vente = new Vente(idVente, pro.getCodebarre(), 1, pro.getPrixVente(),
                            prixHTArt, 0, montantTvaArti, pro.getTva(), pro.getPrixVente(), pro.getLibelle());
                    daoVente.insertNbProdVente(vente);
                    myModelTicket.setMyList(daoTicket.selectTicket(idVente));

                }
            } else {
                JOptionPane.showMessageDialog(this, "Ce produit est déjà dans la liste");
            }
        }


    }//GEN-LAST:event_jButton12ActionPerformed

