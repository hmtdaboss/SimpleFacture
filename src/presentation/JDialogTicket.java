    private void jButtonModifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonModifierActionPerformed

        if (jTableTicket.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(null, "Sélectionnez d'abord une ligne svp !",
                    "Avertissement", JOptionPane.ERROR_MESSAGE);
        } else {
            
            int qte = Integer.parseInt(JOptionPane.showInputDialog(this,
                    "Donner la qte?", null));
            Produit tick = myModelTicket.getMyList(jTableTicket.getSelectedRow());
            if (qte <= 0) {
                daoTicket.deleteNbProduitVendu(tick.getCodebarre(), idVente);

            } else {
                JOptionPane.showMessageDialog(this, tick.getPrixVente());
                daoTicket.updateTicketQte(qte, idVente, tick.getCodebarre(), tick.getPrixVente());
            }
            myModelTicket.setMyList(daoTicket.selectTicket(idVente));
        }
    }//GEN-LAST:event_jButtonModifierActionPerformed

